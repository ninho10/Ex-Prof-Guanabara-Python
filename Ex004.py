# De input em um nome, mostrando ele dentro de algumas função protas

a = input('Digite algo : ')
print(' o tipo primitivo desse valor e : ', type(a))
print(' só tem espaço : ', a.isspace())
print(' e um numero : ', a.isnumeric())
print(' ele e alfabético : ', a.isalpha())
print(' ele e alfanumérico : ', a.isalnum())
print(' esta em MAIÚSCULA : ', a.isupper())
print(' esta em minuscula : ', a.islower())
print(' esta capitalizada : ', a.istitle())
