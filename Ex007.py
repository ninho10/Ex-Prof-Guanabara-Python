# PERGUNTE QUANTO DE KM VAI VIAJAR ATE 200KM PAGUE 0.50 CENTAVOS POR KM SENDO MAIS PARA 0.45

Km = int(input('quantos km tem sua viagem '))
if Km < 200:
    print('Sua passagem vai custar 0.50 centavos por km custar ', Km * 0.50)
else:
    print('Sua passagem vai custar 0.45 centavos por km custar ', Km * 0.45)
